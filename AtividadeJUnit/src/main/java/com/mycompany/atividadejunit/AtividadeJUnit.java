package com.mycompany.atividadejunit;

public class AtividadeJUnit {

    public int metodoAdicao(int x, int y) {
        int soma = x + y;
        return soma;
    }

    public int metodoSubtr(int x, int y) {
        int subtr = x - y;
        return subtr;
    }

    public int metodoDivis(int x, int y) {
        int divis = x / y;
        return divis;
    }

    public int metodoMultipl(int x, int y) {
        int multipl = x * y;
        return multipl;
    }
}
